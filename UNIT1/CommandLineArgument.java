public class CommandLineArgument{
    public static void main(String[] args){
        /*Command line arguments are the arguments which are passed to the main method when we run the program.
        These arguments are passed as an array of strings to the main method. We can access these arguments using the index of the array. The first argument is accessed using args[0], the second argument is accessed using args[1], and so on.*/
        System.out.println(args[0]);
    }
}