// format and printf method , they are used to format the output in java.
public class Program47 {
    public static void main(String[] args) {
        // using format method
         String name1 = "Anshika";
         int age1 = 19;
         System.out.println(String.format("Name: %s, Age: %d", name1, age1));

         //using printf method
         String name2 = "Neelam";
         int age2 = 45;
         System.out.printf("Name: %s, Age: %d", name2 , age2);
    }
}